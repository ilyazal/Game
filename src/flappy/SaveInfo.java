package flappy;


public class SaveInfo {
    private String name;
    private int score;

    public SaveInfo() {
    }

    public SaveInfo(String name, int score) {
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
